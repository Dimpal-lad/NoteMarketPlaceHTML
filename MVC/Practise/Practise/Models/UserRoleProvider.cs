        public override string[] GetRolesForUser(string username)
        {
            using(NotesMarketPlaceEntities2 dbObj=new NotesMarketPlaceEntities2())
            {
                var userRole = (from user in dbObj.tblUsers
                                join rolemap in dbObj.tblUserRoles
                                on user.RoleID equals rolemap.ID
                                where user.EmailID==username
                                select rolemap.Name).ToArray();
                return userRole;
            }
           
